int VulkanRenderer::init(GLFWwindow* newWindow)
{
	window = newWindow;
	try {
		createInstance();
		createDebugCallback();
		createSurface();
		getPhysicalDevice();
		createLogicalDevice();
		createSwapChain();
		createRenderPass();
		createDescriptorSetLayout();
		createGraphicsPipeline();
		createFramebuffers();
		createCommandPool();

		uboViewProjection.projection = glm::perspective(glm::radians(45.0f), (float)swapChainExtent.width / (float)swapChainExtent.height, 0.1f, 100.0f);
		uboViewProjection.view = glm::lookAt(glm::vec3(0.0f, 0.0f, 2.0f), glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, 1.0f, 0.0f));
		uboViewProjection.projection[1][1] *= -1;

		vector<Vertex> meshVertices1 = {
			{{0.1, -0.4, 0.0}, {1.0f, 0.0f, 0.0f}},
			{{0.1, 0.4, 0.0}, {0.0f, 1.0f, 0.0f}},
			{{-0.9, 0.4, 0.0}, {0.0f, 0.0f, 1.0f}},
			{{-0.9, -0.4, 0.0}, {1.0f, 1.0f, 0.0f}},
		};

		vector<Vertex> meshVertices2 = {
			{{0.9, -0.3, 0.0}, {1.0f, 0.0f, 0.0f}},
			{{0.9, 0.1, 0.0}, {0.0f, 1.0f, 0.0f}},
			{{0.1, 0.1, 0.0}, {0.0f, 0.0f, 1.0f}},
			{{0.1, -0.3, 0.0}, {1.0f, 1.0f, 0.0f}},
		};

		vector<uint32_t> meshIndices = {
			0, 1, 2,
			2, 3, 0

		};

		Mesh firstMesh = Mesh(mainDevice.physicalDevice, mainDevice.logicalDevice, graphicsQueue, graphicsCommandPool, &meshVertices1, &meshIndices);
		Mesh secondMesh = Mesh(mainDevice.physicalDevice, mainDevice.logicalDevice, graphicsQueue, graphicsCommandPool, &meshVertices2, &meshIndices);
		meshList.push_back(firstMesh);
		meshList.push_back(secondMesh);

		createCommandBuffers();
		createUniformBuffers();
		createDescriptorPool();
		createDescriptorSets();
		recordCommands();
		createSynchronization();
	}
	catch (runtime_error& e) {
		printf("ERROR: %s\n", e.what());
		return EXIT_FAILURE;
	}

	return 0;
}

